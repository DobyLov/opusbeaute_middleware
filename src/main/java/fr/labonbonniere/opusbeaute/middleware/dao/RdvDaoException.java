package fr.labonbonniere.opusbeaute.middleware.dao;

public class RdvDaoException extends Exception {

	private static final long serialVersionUID = 1L;

	public RdvDaoException() {
		super();
	}

	public RdvDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public RdvDaoException(String message) {
		super(message);
	}

	public RdvDaoException(Throwable cause) {
		super(cause);
	}

}