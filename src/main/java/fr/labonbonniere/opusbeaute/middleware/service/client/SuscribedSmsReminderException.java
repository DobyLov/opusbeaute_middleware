package fr.labonbonniere.opusbeaute.middleware.service.client;

public class SuscribedSmsReminderException extends Exception {

	private static final long serialVersionUID = 1L;

	public SuscribedSmsReminderException() {
		super();
	}

	public SuscribedSmsReminderException(String message) {
		super(message);
	}

	public SuscribedSmsReminderException(Throwable cause) {
		super(cause);
	}

	public SuscribedSmsReminderException(String message, Throwable cause) {
		super(message, cause);
	}

}
