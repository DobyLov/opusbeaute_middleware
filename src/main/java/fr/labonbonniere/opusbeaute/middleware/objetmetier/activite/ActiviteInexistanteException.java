package fr.labonbonniere.opusbeaute.middleware.objetmetier.activite;

public class ActiviteInexistanteException extends Exception {

	private static final long serialVersionUID = 1L;

	public ActiviteInexistanteException() {
		super();
	}

	public ActiviteInexistanteException(String message, Throwable cause) {
		super(message, cause);
	}

	public ActiviteInexistanteException(String message) {
		super(message);
	}

	public ActiviteInexistanteException(Throwable cause) {
		super(cause);
	}
	
	

}
